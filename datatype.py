#string
name = "Sidra"
print(name)  # Output: Ramisa

#integer
age = 24
print(age)  # Output: 19

#float
height = 6.4   
print(height)   #Output:5.0

#boolean
is_student = True
print(is_student)  # Output: True

is_teacher = False
print(is_teacher)   #Output: False

#list
fruits = ["apple","mango","banana","strawberry"]
print(fruits)  # Output: ["apple","mango","banana","strawberry"]

# tuple
flowers = ("Rose", "Tulip", "Lily", "Sunflower")
print(flowers)  # Output: ("Rose", "Tulip", "Lily", "Sunflower")

# dictionary
my_info = {"name": "Sidra", "age": 24, "slot": "Monday 2-5"}
print(my_info)  # Output: {"name": "Ramsia", "age": 19, "slot": "Monday 2-5"}

# set
languages = {"HTML", "CSS", "TypeScript", "JavaScript", "Next.js"}
print(languages)    # Output: {"HTML", "CSS", "TypeScript", "JavaScript", "Next.js"}

#None
x = None
print(x)  # Output: None